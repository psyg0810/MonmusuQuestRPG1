#==============================================================================
# ■ CasinoManager
#==============================================================================
module CasinoManager; end
class << CasinoManager
  #--------------------------------------------------------------------------
  # ● セットアップ
  #--------------------------------------------------------------------------
  def setup(minimum_coin)
    @minimum_coin = minimum_coin
    @bet_num = 0
  end
  #--------------------------------------------------------------------------
  # ● 最大ベットの取得
  #--------------------------------------------------------------------------
  def bet_max
    return 5
  end  
  #--------------------------------------------------------------------------
  # ● 賭け金を増やす
  #--------------------------------------------------------------------------
  def add_bet
    return unless @bet_num < bet_max
    @bet_num += 1
    if over_bet? 
      @bet_num -= 1
    else
      NWConst::Casino::BET_SE.play
    end
  end
  #--------------------------------------------------------------------------
  # ● 賭け金を減らす
  #--------------------------------------------------------------------------
  def sub_bet
    return unless 0 < @bet_num
    @bet_num -= 1
    NWConst::Casino::BET_SE.play
  end
  #--------------------------------------------------------------------------
  # ● ライン数を増やす
  #--------------------------------------------------------------------------
  def add_line
    return unless @bet_num < bet_max
    @bet_num += 1
  end
  #--------------------------------------------------------------------------
  # ● ベット回数の取得
  #--------------------------------------------------------------------------
  def bet_num
    return @bet_num
  end
  #--------------------------------------------------------------------------
  # ● 最低賭け金の取得
  #--------------------------------------------------------------------------
  def minimum_coin
    return @minimum_coin
  end
  #--------------------------------------------------------------------------
  # ● 賭け金の取得
  #--------------------------------------------------------------------------
  def bet_coin
    return @bet_num * @minimum_coin
  end
  #--------------------------------------------------------------------------
  # ● 賭け金が所持コインを上回っている？
  #--------------------------------------------------------------------------
  def over_bet?
    return $game_party.coin < bet_coin
  end
end

