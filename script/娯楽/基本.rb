#==============================================================================
# ■ Game_Party
#==============================================================================
class Game_Party < Game_Unit
  #--------------------------------------------------------------------------
  # ● 公開インスタンス変数
  #--------------------------------------------------------------------------
  attr_reader   :coin
  #--------------------------------------------------------------------------
  # ○ オブジェクト初期化
  #--------------------------------------------------------------------------
  alias nw_casino_initialize initialize
  def initialize
    nw_casino_initialize
    @coin = 0
  end
  #--------------------------------------------------------------------------
  # ● 所持コインの増加（減少）
  #--------------------------------------------------------------------------
  def gain_coin(amount)
    @coin = [[@coin + amount, 0].max, max_coin].min
  end
  #--------------------------------------------------------------------------
  # ● 所持コインの減少
  #--------------------------------------------------------------------------
  def lose_coin(amount)
    gain_coin(-amount)
  end
  #--------------------------------------------------------------------------
  # ● 所持コインの最大値を取得
  #--------------------------------------------------------------------------
  def max_coin
    return 99999999
  end
end

#==============================================================================
# ■ Window_Coin
#==============================================================================
class Window_Coin < Window_Base
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super(Graphics.width - window_width, Graphics.height - window_height, window_width, window_height)
    self.z = 200
    @coin  = 0
    @interval = 0
    refresh
  end
  #--------------------------------------------------------------------------
  # ● ウィンドウ幅の取得
  #--------------------------------------------------------------------------
  def window_width
    return 160
  end
  #--------------------------------------------------------------------------
  # ● ウィンドウ高さの取得
  #--------------------------------------------------------------------------
  def window_height
    return fitting_height(2)
  end
  #--------------------------------------------------------------------------
  # ● 更新処理
  #--------------------------------------------------------------------------
  def update
    super
    update_have_coin
  end
  #--------------------------------------------------------------------------
  # ● リフレッシュ
  #--------------------------------------------------------------------------
  def refresh
    contents.clear
    draw_have_coin
    draw_bet_coin
    @coin = $game_party.coin
  end
  #--------------------------------------------------------------------------
  # ● 所持コインの更新処理
  #--------------------------------------------------------------------------
  def update_have_coin
    unless @coin == $game_party.coin
      sub = $game_party.coin - @coin
      @coin += 0 < sub ? [1, sub / 5].max : [-1, sub / 5].min
      rect  = Rect.new(0, 0, contents.width, line_height)      
      contents.clear_rect(rect)
      change_color(system_color)
      draw_text(rect, "持", 0)
      change_color(normal_color)
      draw_text(rect, "#{@coin}枚", 2)      
      NWConst::Casino::COIN_SE.play if (@interval % 10) == 0
      @interval += 1
    else
      @interval = 0
    end
  end  
  #--------------------------------------------------------------------------
  # ● 所持コインの描画
  #--------------------------------------------------------------------------
  def draw_have_coin
    rect  = Rect.new(0, 0, contents.width, line_height)
    value = $game_party.coin
    change_color(system_color)
    draw_text(rect, "持", 0)
    change_color(normal_color)
    draw_text(rect, "#{value}枚", 2)
  end  
  #--------------------------------------------------------------------------
  # ● 賭けコインの描画
  #--------------------------------------------------------------------------
  def draw_bet_coin
    rect  = Rect.new(0, line_height, contents.width, line_height)
    value = CasinoManager.bet_coin
    change_color(system_color)
    draw_text(rect, "賭", 0)
    change_color(normal_color)
    draw_text(rect, "#{value}枚", 2)
  end  
end

