# ■ Scene_CasinoBase
#==============================================================================
class Scene_CasinoBase < Scene_Base
  #--------------------------------------------------------------------------
  # ● 開始処理
  #--------------------------------------------------------------------------
  def start
    super    
    create_background
    create_all_window
    change_phase(:stand)
  end
  #--------------------------------------------------------------------------
  # ● 終了処理
  #--------------------------------------------------------------------------
  def terminate
    super
    dispose_background
  end
  #--------------------------------------------------------------------------
  # ● フェイズの変更
  #--------------------------------------------------------------------------
  def change_phase(phase)
    @phase = phase
  end
  #--------------------------------------------------------------------------
  # ● 背景の作成
  #--------------------------------------------------------------------------
  def create_background
    @background_sprite = Sprite.new
    @background_sprite.bitmap = Cache.casino("casino_bg")
  end
  #--------------------------------------------------------------------------
  # ● 全ウィンドウの作成
  #--------------------------------------------------------------------------
  def create_all_window
    create_desc_window
    create_coin_window
  end
  #--------------------------------------------------------------------------
  # ● 記述ウィンドウの作成
  #--------------------------------------------------------------------------
  def create_desc_window
    @desc_window = Window_CasinoDesc.new
  end
  #--------------------------------------------------------------------------
  # ● コインウィンドウの作成
  #--------------------------------------------------------------------------
  def create_coin_window
    @coin_window = Window_Coin.new
  end
  #--------------------------------------------------------------------------
  # ● 背景の解放
  #--------------------------------------------------------------------------
  def dispose_background
    @background_sprite.dispose
  end
  #--------------------------------------------------------------------------
  # ● 更新処理
  #--------------------------------------------------------------------------
  def update
    super
    case @phase
    when :stand;  update_stand
    when :play;   update_play
    end
  end
  #--------------------------------------------------------------------------
  # ● 待機時の更新処理
  #--------------------------------------------------------------------------
  def update_stand
    if Input.trigger?(:RIGHT)
      CasinoManager.add_bet
      @coin_window.refresh
    elsif Input.trigger?(:LEFT)
      CasinoManager.sub_bet
      @coin_window.refresh
    end
    return_scene if Input.trigger?(:B)
  end
  #--------------------------------------------------------------------------
  # ● ゲーム中の更新処理
  #--------------------------------------------------------------------------
  def update_play
  end
  #--------------------------------------------------------------------------
  # ● フレーム更新（ウェイト用）
  #--------------------------------------------------------------------------
  def update_for_wait
    update_basic
  end
  #--------------------------------------------------------------------------
  # ● フレーム更新（ウェイト用）
  #--------------------------------------------------------------------------
  def wait(duration)
    update_basic
  end
  #--------------------------------------------------------------------------
  # ● ウェイト
  #--------------------------------------------------------------------------
  def wait(duration)
    duration.times {|i| update_for_wait if i < duration / 2 || !show_fast? }
  end
  #--------------------------------------------------------------------------
  # ● 早送り判定
  #--------------------------------------------------------------------------
  def show_fast?
    Input.press?(:A) || Input.press?(:C)
  end
end

